const replacementsByLanguage: Record<DashboardLanguage, Array<[string, string]>> = {
  pt: [],
  en: phraseEntries.flatMap((entry) => entry.source.map((source) => [source, entry.en] as [string, string])),
  es: phraseEntries.flatMap((entry) => entry.source.map((source) => [source, entry.es] as [string, string])),
};

function tidyDashboardText(text: string) {
  return text
    .replace(/^(\d{2})([A-Za-z])/, "$1 $2")
    .replace(/^([A-Z]\d{2})([A-Za-z])/, "$1 $2")
    .replace(/\s{2,}/g, " ")
    .trim();
}

export function translateDashboardText(text: string, language: DashboardLanguage) {
  if (language === "pt" || !text) return text;

  let translated = text;
  const replacements = [...replacementsByLanguage[language]].sort((a, b) => b[0].length - a[0].length);
  for (const [source, target] of replacements) {
    translated = replaceAllLiteral(translated, source, target);
  }

  return tidyDashboardText(translated);
}

export function useTranslation() {
  const { language } = useLanguage();

  return {
    language,
    t: (key: string) => translateDashboardText(key, language),
    translateText: (key: string) => translateDashboardText(key, language),
  };
}
