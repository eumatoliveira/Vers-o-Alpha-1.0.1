function escapeRegExp(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function applyMatchCase(match: string, replacement: string) {
  if (match === match.toUpperCase()) return replacement.toUpperCase();
  if (match === match.toLowerCase()) return replacement.toLowerCase();
  return replacement;
}

function replaceAllLiteral(text: string, search: string, replacement: string) {
  return text.replace(new RegExp(escapeRegExp(search), "giu"), (match) => applyMatchCase(match, replacement));
}

